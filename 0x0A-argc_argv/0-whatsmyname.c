#include <stdio.h>
#include <stdlib.h>

/**
 * main - print the name of the program followed by a new line
 * @argc: argument count
 * @argv: array pointing to arguments
 * Return: Name of program with its path
 */
int main(int argc, char *argv[])
{
	argc = 0;
	printf("%s\n", argv[argc]);
	return (0);
}
